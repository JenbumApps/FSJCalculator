public class Calculator {

    /**
     * Addition
     */
    public static int add(int val1, int val2){
        return val1 + val2;
    }

    public static float add(float val1, float val2){
        return val1 + val2;
    }

    public static double add(double val1, double val2){
        return val1 + val2;
    }

    /**
     *
     * Subtraction
     */
    public static int subtract(int val1, int val2){
        return val1 - val2;
    }

    public static float subtract(float val1, float val2){
        return val1 - val2;
    }

    public static double subtract(double val1, double val2){
        return val1 - val2;
    }

    /**
     *
     * Multiplication
     */
    public static int multiply(int val1, int val2){
        return val1 * val2;
    }

    public static float multiply(float val1, float val2){
        return val1 * val2;
    }

    public static double multiply(double val1, double val2){
        return val1 * val2;
    }


    /**
     *
     * Division
     */
    public static int divide(int val1, int val2){
        return val1 / val2;
    }

    public static float divide(float val1, float val2){
        return val1 / val2;
    }

    public static double divide(double val1, double val2){
        return val1 / val2;
    }
}
